import 'package:flutter/material.dart';

// font 100
h100(text, double size,
        {Color color = const Color(0xffD9D9D9),
        TextAlign align = TextAlign.left}) =>
    Text(
      text,
      textAlign: align,
      style: TextStyle(
        fontSize: size,
        fontWeight: FontWeight.w100,
        color: color,
      ),
    );

